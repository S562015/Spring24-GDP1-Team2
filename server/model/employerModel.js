import mongoose from "mongoose";
const { Schema } = mongoose;

const employerSchema = new Schema({
  organizationId: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  userName: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

const EmployerModel = mongoose.model("employer", employerSchema);

export default EmployerModel;
